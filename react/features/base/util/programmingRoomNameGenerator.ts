/**
 * Returns a random string from the provided array.
 *
 * @param {Array<string>} arr - The array to choose from.
 * @returns {string} - The random string.
 */
function getRandomElement(arr: string[]): string {
    return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * Generates a random room name using programming terminology.
 *
 * @returns {string} - The generated room name.
 */
export function generateProgrammingRoomName(): string {
    const adjective = getRandomElement(PROGRAMMING_ADJECTIVES);
    const noun = getRandomElement(PROGRAMMING_NOUNS);
    const verb = getRandomElement(PROGRAMMING_NOUNS); // Using nouns as verbs often works in tech (e.g., "Code") or just double nouns

    return `${adjective}${noun}${verb}`;
}
